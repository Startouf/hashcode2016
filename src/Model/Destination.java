package Model;

public class Destination {

	private int posX = -1;
	private int posY = -1;

	public Destination() {
		super();
	}

	public int getPosX() {
		return posX;
	}

	public void setPosX(int posX) {
		this.posX = posX;
	}

	public int getPosY() {
		return posY;
	}

	public void setPosY(int posY) {
		this.posY = posY;
	}

}